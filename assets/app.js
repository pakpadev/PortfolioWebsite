const STORAGE_KEY = "portfolio-language";

const root = document.documentElement;
const header = document.querySelector("[data-header]");
const nav = document.querySelector("[data-nav]");
const navToggle = document.querySelector("[data-nav-toggle]");
const typewriter = document.querySelector("[data-typewriter]");
const heroCanvas = document.querySelector("[data-particles]");
const descriptionMeta = document.querySelector('meta[name="description"]');
const ogDescriptionMeta = document.querySelector('meta[property="og:description"]');
const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const reduceGeneralMotion = prefersReducedMotion;
const reduceHeroMotion = false;

let currentLanguage = getInitialLanguage();
let typewriterTimer = null;

function getInitialLanguage() {
  const saved = window.localStorage.getItem(STORAGE_KEY);
  if (saved && translations[saved]) return saved;

  const browserLanguage = (navigator.language || "ja").toLowerCase();
  return browserLanguage.startsWith("ja") ? "ja" : "en";
}

function updateHeaderState() {
  if (!header) return;
  header.classList.toggle("is-scrolled", window.scrollY > 24);
}

function closeNav() {
  if (!nav || !header || !navToggle) return;
  nav.classList.remove("is-open");
  header.classList.remove("menu-open");
  navToggle.setAttribute("aria-expanded", "false");
  navToggle.setAttribute("aria-label", translations[currentLanguage].nav_open);
}

function applyTranslations(language) {
  const copy = translations[language];

  root.lang = copy.html_lang;
  document.title = copy.page_title;
  if (descriptionMeta) descriptionMeta.setAttribute("content", copy.page_description);
  if (ogDescriptionMeta) ogDescriptionMeta.setAttribute("content", copy.page_description);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (copy[key]) element.textContent = copy[key];
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const pairs = element.dataset.i18nAttr.split("|");
    pairs.forEach((pair) => {
      const [attribute, key] = pair.split(":");
      if (attribute && key && copy[key]) {
        element.setAttribute(attribute, copy[key]);
      }
    });
  });

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    const isActive = button.dataset.langButton === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  if (navToggle) {
    const isOpen = navToggle.getAttribute("aria-expanded") === "true";
    navToggle.setAttribute("aria-label", isOpen ? copy.nav_close : copy.nav_open);
  }

  startTypewriter(copy.typewriter_phrases);
}

function startTypewriter(phrases) {
  if (!typewriter) return;

  if (typewriterTimer) {
    window.clearTimeout(typewriterTimer);
    typewriterTimer = null;
  }

  if (reduceHeroMotion) {
    typewriter.textContent = phrases[0];
    return;
  }

  let phraseIndex = 0;
  let charIndex = 0;
  let deleting = false;

  function tick() {
    const phrase = phrases[phraseIndex];
    charIndex += deleting ? -1 : 1;
    typewriter.textContent = phrase.slice(0, charIndex);

    if (!deleting && charIndex === phrase.length) {
      deleting = true;
      typewriterTimer = window.setTimeout(tick, 1450);
      return;
    }

    if (deleting && charIndex === 0) {
      deleting = false;
      phraseIndex = (phraseIndex + 1) % phrases.length;
    }

    typewriterTimer = window.setTimeout(tick, deleting ? 42 : 78);
  }

  typewriter.textContent = phrases[0].slice(0, 1);
  charIndex = 1;
  typewriterTimer = window.setTimeout(tick, 260);
}

function setLanguage(language) {
  if (!translations[language]) return;
  currentLanguage = language;
  window.localStorage.setItem(STORAGE_KEY, language);
  applyTranslations(language);
}

if (navToggle && nav && header) {
  navToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("is-open");
    header.classList.toggle("menu-open", isOpen);
    navToggle.setAttribute("aria-expanded", String(isOpen));
    navToggle.setAttribute(
      "aria-label",
      isOpen ? translations[currentLanguage].nav_close : translations[currentLanguage].nav_open
    );
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeNav);
  });
}

document.querySelectorAll("[data-lang-button]").forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.langButton);
  });
});

window.addEventListener("scroll", updateHeaderState, { passive: true });
updateHeaderState();
applyTranslations(currentLanguage);

window.requestAnimationFrame(() => {
  document.body.classList.add("hero-animate");
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;

      entry.target.classList.add("is-visible");
      entry.target.querySelectorAll(".reveal-child").forEach((child, index) => {
        child.style.setProperty("--reveal-delay", `${index * 90}ms`);
        child.classList.add("is-visible");
      });

      revealObserver.unobserve(entry.target);
    });
  },
  { threshold: 0.16 }
);

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

if (nav) {
  const sectionObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        nav.querySelectorAll("a").forEach((link) => {
          link.classList.toggle("is-active", link.getAttribute("href") === `#${entry.target.id}`);
        });
      });
    },
    { rootMargin: "-42% 0px -48% 0px", threshold: 0 }
  );

  document.querySelectorAll("main section[id]").forEach((section) => sectionObserver.observe(section));
}

if (heroCanvas && !reduceHeroMotion) {
  const context = heroCanvas.getContext("2d");
  let width = 0;
  let height = 0;
  let rafId = 0;
  let particles = [];
  let pointer = {
    x: 0,
    y: 0,
    active: false,
  };

  function resizeHeroCanvas() {
    const ratio = Math.min(window.devicePixelRatio || 1, 1.6);
    width = heroCanvas.offsetWidth;
    height = heroCanvas.offsetHeight;
    heroCanvas.width = Math.floor(width * ratio);
    heroCanvas.height = Math.floor(height * ratio);
    context.setTransform(ratio, 0, 0, ratio, 0, 0);
  }

  function createParticles() {
    const count = width < 768 ? 28 : 48;
    particles = Array.from({ length: count }, () => ({
      x: Math.random() * width,
      y: Math.random() * height,
      vx: (Math.random() - 0.5) * 0.18,
      vy: (Math.random() - 0.5) * 0.18,
      radius: Math.random() * 1.8 + 0.6,
      alpha: Math.random() * 0.4 + 0.25,
    }));
  }

  function drawParticles() {
    context.clearRect(0, 0, width, height);

    for (let i = 0; i < particles.length; i += 1) {
      const particle = particles[i];
      particle.x += particle.vx;
      particle.y += particle.vy;

      if (particle.x < -12) particle.x = width + 12;
      if (particle.x > width + 12) particle.x = -12;
      if (particle.y < -12) particle.y = height + 12;
      if (particle.y > height + 12) particle.y = -12;

      if (pointer.active) {
        const dx = pointer.x - particle.x;
        const dy = pointer.y - particle.y;
        const distance = Math.hypot(dx, dy);
        if (distance < 120 && distance > 0) {
          particle.x -= (dx / distance) * 0.18;
          particle.y -= (dy / distance) * 0.18;
        }
      }

      context.beginPath();
      context.arc(particle.x, particle.y, particle.radius, 0, Math.PI * 2);
      context.fillStyle = `rgba(0, 245, 255, ${particle.alpha})`;
      context.fill();
    }

    for (let i = 0; i < particles.length; i += 1) {
      for (let j = i + 1; j < particles.length; j += 1) {
        const dx = particles[i].x - particles[j].x;
        const dy = particles[i].y - particles[j].y;
        const distance = Math.hypot(dx, dy);
        if (distance > 96) continue;

        context.beginPath();
        context.moveTo(particles[i].x, particles[i].y);
        context.lineTo(particles[j].x, particles[j].y);
        context.strokeStyle = `rgba(123, 47, 255, ${(1 - distance / 96) * 0.16})`;
        context.lineWidth = 0.7;
        context.stroke();
      }
    }

    rafId = window.requestAnimationFrame(drawParticles);
  }

  function resetHeroAnimation() {
    window.cancelAnimationFrame(rafId);
    resizeHeroCanvas();
    createParticles();
    drawParticles();
  }

  heroCanvas.addEventListener("pointermove", (event) => {
    const rect = heroCanvas.getBoundingClientRect();
    pointer.x = event.clientX - rect.left;
    pointer.y = event.clientY - rect.top;
    pointer.active = true;
  });

  heroCanvas.addEventListener("pointerleave", () => {
    pointer.active = false;
  });

  window.addEventListener("resize", resetHeroAnimation, { passive: true });
  resetHeroAnimation();
}
